package tsu.mk.lexis;

/* {@code INT_LITERAL} represents 'k'; {@code IDENTIFIER} represents 'a' in the parsing table */
public enum TokenType {
	LEFT_PAREN,
	RIGHT_PAREN,
	LEFT_BRACKET,
	RIGHT_BRACKET,
	LEFT_BRACE,
	RIGHT_BRACE,
	COMMA,
	MINUS,
	PLUS,
	SEMICOLON,
	SLASH,
	STAR,
	EQUAL,
	IDENTIFIER,
	WHILE,
	IF,
	ELSE,
	INPUT,
	OUTPUT,
	ARRAY,
	INT_TYPE,
	INT_LITERAL,
	GREATER,
	LESS,
	EOF;
}
